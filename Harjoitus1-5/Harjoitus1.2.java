import java.text.DecimalFormat;
import java.util.Scanner;

public class Bensa {

	public static void main(String[] args) {
		
		Scanner input = new Scanner(System.in);
		DecimalFormat desimaalit = new DecimalFormat("0.00");
		
		double tankkaus, litrahinta, kilometrilaskuri;
		int ajetut = 0;
		
		System.out.print("Anna tankattu määrä: ");
		tankkaus = input.nextDouble();
		System.out.print("Anna ajetut kilometrit: ");
		ajetut = input.nextInt();
		System.out.print("Anna litrahinta: ");
		litrahinta = input.nextDouble();
		kilometrilaskuri = tankkaus / ajetut * litrahinta;
		System.out.println("Ajo per kilometri maksaa " + desimaalit.format(kilometrilaskuri));
		
	}

}
